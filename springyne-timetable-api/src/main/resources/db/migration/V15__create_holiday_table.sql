CREATE TABLE IF NOT EXISTS HOLIDAYS (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR(255) NOT NULL,
    YEAR_STARTS INTEGER,
    YEAR_ENDS INTEGER,
    MONTH_STARTS INTEGER,
    MONTH_ENDS INTEGER,
    DAY_STARTS INTEGER,
    DAY_ENDS INTEGER,
    REPEATS BOOLEAN DEFAULT FALSE,
    DELETED BOOLEAN DEFAULT FALSE
    );